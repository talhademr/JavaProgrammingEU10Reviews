package week07_10_22_2022;

public class NestedLoopIntro {
    public static void main(String[] args) {

        for (int i = 1; i <= 3; i++) {

            for (int j = 1; j <= 3; j++) {
                System.out.println("outer loop :" + i + "inner loop  :" + j);
            }

        }

        /*
         *******
         *******
         *******
         *******
         */

        System.out.println("-----------------");

        for (int i = 0; i < 4; i++) {

            for (int j = 0; j < 7; j++) {
                System.out.print("*");

            }
            System.out.println();
        }


        System.out.println("------------------");
// yukarıdakini tek loop ile yapmayı dene





        }

    }


